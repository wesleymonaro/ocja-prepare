//Operadores unários e atribuições
package aula3;

public class Programa6 {
	public static void main(String[] args) {
		int m = 20;
		System.out.println(m);

		m = 20;
		int n = m++;
		System.out.println(m);
		System.out.println(n);

		m = 20;
		int o = ++m;
		System.out.println(m);
		System.out.println(o);
	}
}
