package aula3;

public class Programa4 {
	public static void main(String[] args) {
		int c = 10;
		System.out.println(c);
		System.out.println(c++ + --c - c-- + c++ + c);
		c = 10;
		System.out.println(++c + ++c + c++ + c);
		c = 10;
		System.out.println(--c + c++ + ++c - c--);
		System.out.println(c);
	}
}
