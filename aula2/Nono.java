package aula2;

public class Nono {
	public static void main(String[] args) {
		System.out.println(Math.random());
		System.out.println(6*Math.random());
		System.out.println((int)(6*Math.random()));
		/*
		 * Os números no método Random vai de 0 a 5.4, ou seja, se pegarmos um INT desse valor vai de 0 a 5.
		 */
		System.out.println(1+(int)(6*Math.random()));//O 1+ indica que vai de 1 a 6, e assim sucesivamente.
		
		
	}
}
